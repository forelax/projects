function tank(xpos,ypos,angle,color,leftBar,rightBar,id){
	this.x=xpos,this.y=ypos;
	this.id=id;
	this.health=100;
	this.leftBarier=leftBar;
	this.rightBarier=rightBar;
	this.angle=angle;
	this.color=color;
	this.speed=4;
	this.scale=20;
	this.leftKey;
	this.downKey;
	this.rightKey;
	this.upKey;
	this.shootKey;
	this.shots=5;
	this.display=function(){
		ctx.fillStyle=this.color;
		ctx.beginPath();
		ctx.moveTo(this.x,this.y);
		ctx.lineTo(this.x+Math.cos(this.angle+Math.PI/2)*this.scale/3,this.y+Math.sin(this.angle+Math.PI/2)*this.scale/3);
		ctx.lineTo(this.x+Math.cos(this.angle+Math.PI/2)*this.scale/3+Math.cos(this.angle)*this.scale*1.5,this.y+Math.sin(this.angle+Math.PI/2)*this.scale/3+Math.sin(this.angle)*this.scale*1.5);
		ctx.lineTo(this.x+Math.cos(this.angle-Math.PI/2)*this.scale/3+Math.cos(this.angle)*this.scale*1.5,this.y+Math.sin(this.angle-Math.PI/2)*this.scale/3+Math.sin(this.angle)*this.scale*1.5);
		ctx.lineTo(this.x+Math.cos(this.angle-Math.PI/2)*this.scale/3,this.y+Math.sin(this.angle-Math.PI/2)*this.scale/3);
		ctx.lineTo(this.x,this.y);
		ctx.fill();
		ctx.stroke();
		ctx.beginPath();
		ctx.arc(this.x,this.y,this.scale,0,2*Math.PI);
		ctx.fill();
		ctx.stroke();
	}
	this.update=function(){
		if(this.leftKey){
			this.angle-=5*Math.PI/180;
			if(this.angle<0)
				this.angle+=2*Math.PI;
		}
		if(this.rightKey){
			this.angle+=5*Math.PI/180;
			if(this.angle>=2*Math.PI)
				this.angle-=2*Math.PI;
		}
		if(this.upKey){
			this.x+=Math.cos(this.angle)*this.speed;
			this.y+=Math.sin(this.angle)*this.speed;
		}
		if(this.downKey){
			this.x-=Math.cos(this.angle)*this.speed/2;
			this.y-=Math.sin(this.angle)*this.speed/2;
		}
			if(this.x<this.leftBarier+this.scale)
				this.x=this.leftBarier+this.scale;
			if(this.x>this.rightBarier-this.scale)
				this.x=this.rightBarier-this.scale;
			if(this.y<this.scale)
				this.y=this.scale;
			if(this.y>c.height-this.scale)
				this.y=c.height-this.scale;
		if(this.shootKey){
			if(this.shots>0){
				bullets.push(new bullet(this.x+Math.cos(this.angle)*this.scale*1.5,this.y+Math.sin(this.angle)*this.scale*1.5,this.angle,this.id));
				this.shots--;
			}
			this.shootKey=false;
		}
	}
}